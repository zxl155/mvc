     <h2>北京八维教育简介</h2>

	<p>北京八维教育地处北京市海淀区中关村上地信息产业园区，位于首都教育圈（CED）、信息圈（CID）、科技圈（CSD）交汇处，南邻清华、北大、中科院，东接中关村高科技园，北望中关村软件园区。这里企业如织、高校林立、环境优雅、人文气息浓郁。学院教育设施完备，现拥有PC机万余台，专业苹果机240余台，并设有语音教室、投影机房、网络工程实验室、CCIE实验室以及各学院的独立工作室；此外，学院图书馆、形体训练室、体育健身房、多功能报告厅等配套设施也一应俱全。</p>

	<p>北京八维教育以培养工程专业型人才为目标，现设有十大学院：软件工程学院、网络工程学院、游戏学院、建筑学院、传媒学院、高翻学院、国际经贸学院、移动通信学院、云计算学院、基础学院。学院师资力量雄厚，治学态度严谨，一直致力于为社会培养技术人才，国家栋梁。</p>

	<p>“做教育领域开天辟地的巨人”，是八维的愿景。八维将高举新生主义大旗，肩负“创新教育，引领未来”的使命，历经铸身、铸心、铸魂的过程，凭借团队的力量、教育的力量、精神的力量，成为教育领域的巨人。八维将把全部激情和经历奉献给未来，八维也将在伟大的奉献中获得快乐，成就新生。</p>

